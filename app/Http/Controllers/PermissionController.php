    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $request->validate([
            'permission_name' => 'required|unique:permissions|max:255',
        ]);
        $permission = new Permission();
        $permission->permission_name = $request['permission_name'];
        $permission->save();
        if ($permission->save()) {
            return redirect()->route('admin.permissions')->with('success', 'Permission created successfully.');
        }
        return redirect()->route('admin.permissions')->with('error', 'Permission can not be created.');
    }

